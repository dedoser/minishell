#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*new_lst;
	t_list	*elem;

	if (f == NULL && del == NULL)
		return (NULL);
	new_lst = NULL;
	while (lst != NULL)
	{
		if ((elem = ft_lstnew((*f)(lst->content))) == NULL)
		{
			ft_lstclear(&new_lst, del);
			return (NULL);
		}
		ft_lstadd_back(&new_lst, elem);
		lst = lst->next;
	}
	return (new_lst);
}
